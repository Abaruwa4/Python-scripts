# Use len
# Print the length of a string - "hello"
print (len("Hello"))

# String concatenation - join two strings together
print ("Hello" + "World")

# Use upper
print ("hello". upper())

# Use lower
print ("HELLO".lower())

# Explore string indices
print ("Hello" [1])

# Explore splicing
print ("Hello" [1:4])

